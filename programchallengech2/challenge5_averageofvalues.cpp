#include <iostream>
using namespace std;

int main() {
	
	// Data
	double a = 28;
	double b = 32;
	double c = 37;
	double d = 24;
	double e = 33;

	// Sum of 5 values
	double sum = a + b + c + d + e;

	// Average of 5 values
	double average = sum / 5;

	// Display output
	cout << "The average is " << average << endl;

	return 0;
}
